#include "libft.h"

int	ft_atoi(const char *c)
{
	int	i;
	int	rt;
	int	pm;

	rt = 0;
	i = 0;
	pm = 1;
	if (c[i] == '\0')
		return (0);
	while (c[i] && (c[i] == ' ' || c[i] == '\n' || c[i] == '\t'
				|| c[i] == '\v' || c[i] == '\f' || c[i] == '\r'))
		i++;
	if (c[i] && (c[i] == '-' || c[i] == '+'))
	{
		if (c[i] == '-')
			pm *= -1;
		i++;
	}
	while (c[i] && c[i] >= '0' && c[i] <= '9')
	{
		rt = rt * 10 + (c[i] - 48);
		i++;
	}
	return (rt * pm);
}
