#include "libft.h"

int		ft_atoi(const char *c)
{
	int			i;
	long long	rt;
	int			pm;

	rt = 0;
	i = 0;
	pm = 1;
	while (c[i] && (c[i] == ' ' || (c[i] >= 9 && c[i] <= 13)))
		i++;
	if (c[i] && (c[i] == '-' || c[i] == '+'))
	{
		if (c[i] == '-')
			pm *= -1;
		i++;
	}
	while (c[i] && c[i] >= '0' && c[i] <= '9')
	{
		rt = rt * 10 + (c[i] - 48);
		if (rt * pm > 2147483647)
			return (-1);
		else if (rt * pm < -2147483648)
			return (0);
		i++;
	}
	return (rt * pm);
}
